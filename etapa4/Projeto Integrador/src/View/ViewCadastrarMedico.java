    public boolean cadastraMedico() {
        try {
            medico.setNome(txtNome.getText());
            medico.setCrm(txtCrm.getText());
            String especialidadeSelecionada = (String) jComboBoxEspecialidade.getSelectedItem();
            var daoEpescialidade = new EspecialidadeDAO();
            
            int idEsp = daoEpescialidade.buscarEspecialidadePorNome(especialidadeSelecionada).getId();
            
            medico.setEspecialidadeId(idEsp);
            daoMedico.cadastrarMedico(medico);
            limpar();
            JOptionPane.showMessageDialog(this, "Cadastro realizado");
            return true;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Cadastro nÃ£o realizado");
            return false;
        }
    }

    public void popularComboBoxEspecialidades() {
    EspecialidadeDAO daoEspecialidade = new EspecialidadeDAO();
    try {
        List<Especialidade> especialidades = daoEspecialidade.listarEspecialidades();
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        for (Especialidade especialidade : especialidades) {
            model.addElement(especialidade.toString());
        }
        jComboBoxEspecialidade.setModel(model);
    } catch (SQLException e) {
        e.printStackTrace();
    }
}
    public void limpar(){
        txtCrm.setText("");
        txtNome.setText("");
    }

