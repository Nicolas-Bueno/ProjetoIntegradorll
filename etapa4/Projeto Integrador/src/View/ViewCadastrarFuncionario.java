    public boolean cadastrafuncionario() {
        try {
            funcionario.setNome(txtNome.getText());
            funcionario.setCpf(txtCpf.getText());
            funcionario.setEmail(txtEmail.getText());
            funcionario.setLogin(txtLogin.getText());
            funcionario.setSenha(txtSenha.getText());
            daoFuncionario.cadastrarFuncionario(funcionario);
            JOptionPane.showMessageDialog(this, "Cadastro realizado");
            limparCampos();
            return true;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Cadastro n√£o realizado");
            return false;
        }
    }

    public void limparCampos() {
        txtNome.setText("");
        txtCpf.setText("");
        txtEmail.setText("");
        txtLogin.setText("");
        txtSenha.setText("");
    }

